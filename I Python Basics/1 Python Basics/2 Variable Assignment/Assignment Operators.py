a = 10
b = 26

# Add and Assign
a += b
print(f"a+=b => {a}") # F/f PREFIX FOR FORMATTED STRING / f-string
# Subtract and Assign
a -= b
print(f"a-=b => {a}")
# Multiply and Assign
a *= b
print(f"a*=b => {a}")
# Divide and Assign
a /= b
print(f"a/=b => {a}")
# Modulus and Assign
a %= b
print(f"a%=b => {a}")
# Exponent and Assign
a **= b
print(f"a**=b => {a}")
# Floor Division and Assign
a //= b
print(f"a//=b => {a}")